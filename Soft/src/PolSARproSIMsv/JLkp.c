int	Jlookup		(double x, int Nmax, double *pJ, Jn_Lookup *pJLkp)
{
 const double	b		= 0.63661977236758;
 const double	pi4		= 0.78539816339745;
 int			n;
 int			m;
 double			ax	= fabs(x);
 double			sbax, px, qx, arg;
 double			xm;
 double			dn;
 double			twoxinv;
 double			*pJarray;

 if (Nmax >9) {
  Nmax = 9;
 }
 if (ax > FLT_EPSILON) {
  m	= (int) (ax/pJLkp->dx);
  if (m>pJLkp->nmax-2) {
   /******************************************************************/
   /* Use asymptotic form for J0, and J1 and recurrence for J2 -> Jn */
   /******************************************************************/
   sbax		= sqrt(b/ax);
   for (n=0; n<=1; n++) {
    dn		= (double) n;
    px		= 1.0-(dn-1.0)*(dn-9.0)/(128.0*ax*ax);
    qx		= (dn-1.0)/(8.0*ax)-(dn-1.0)*(dn-9.0)*(dn-25.0)/(3072.0*ax*ax*ax);
    arg		= ax-(2.0*dn+1.0)*pi4;
    pJ[n]	= sbax*(px*cos(arg)-qx*sin(arg));
   }
   if (x < 0.0) {
    pJ[1]	= -pJ[1];
   }
   twoxinv	= 2.0/x;
   for (n=2; n<=Nmax; n++) {
    pJ[n]	= twoxinv*(n-1)*pJ[n-1] - pJ[n-2];
   }
  } else {
   /**********************************/
   /* Use interpolation for J0 -> Jn */
   /**********************************/
    xm		 = m*pJLkp->dx;
    arg		 = (ax-xm)/(2.0*pJLkp->dx);
    m		+= 2;
	for (n=0; n<=Nmax; n++) {
	 pJarray	= pJLkp->pJ[n];
	 pJ[n]		= pJarray[m]+arg*(pJarray[m+1]-pJarray[m-1]
				+ 0.5*arg*(pJarray[m+2]+pJarray[m-2]-2.0*pJarray[m]));
	}
    if (x < 0.0) {
	 for (n=1; n<=Nmax; n+=2) {
      pJ[n]	= -pJ[n];
	 }
    }
  }
 } else {
  /********************/
  /* Argument is zero */
  /********************/
  pJ[0]	= 1.0;
  for (n=1; n<=Nmax; n++) {
   pJ[n]	= 0.0;
  }
 }
 return (NO_JLKP_ERRORS);
}
